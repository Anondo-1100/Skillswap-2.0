// Adding teachingMode and price to all skills
export const SKILLS_DATA: Skill[] = INITIAL_SKILLS.map((skill): Skill => {
  // More balanced distribution: 40% chance of being paid
  const isPaid = skill.id % 5 <= 1;
  return {
    ...skill,
    teachingMode: isPaid ? 'paid' : 'swap',
    // Prices now vary based on level and category
    ...(isPaid && {
      price: skill.level === 'advanced' ?
        Math.floor(Math.random() * (100 - 70) + 70) :
        skill.level === 'intermediate' ?
          Math.floor(Math.random() * (69 - 40) + 40) :
          Math.floor(Math.random() * (39 - 25) + 25)
    })
  };
});